// Generate heatmap points inside the polygon
const generateHeatmapPoints = (
  polygon: LatLng[],
  density: number = 20
): { latitude: number; longitude: number; weight: number }[] => {
  const bounds = polygon.reduce(
    (bounds, coord) => {
      bounds.minLat = Math.min(bounds.minLat, coord.latitude);
      bounds.maxLat = Math.max(bounds.maxLat, coord.latitude);
      bounds.minLng = Math.min(bounds.minLng, coord.longitude);
      bounds.maxLng = Math.max(bounds.maxLng, coord.longitude);
      return bounds;
    },
    {
      minLat: Infinity,
      maxLat: -Infinity,
      minLng: Infinity,
      maxLng: -Infinity,
    }
  );

  const heatmapPoints: {
    latitude: number;
    longitude: number;
    weight: number;
  }[] = [];
  const latStep = (bounds.maxLat - bounds.minLat) / density;
  const lngStep = (bounds.maxLng - bounds.minLng) / density;

  for (let lat = bounds.minLat; lat <= bounds.maxLat; lat += latStep) {
    for (let lng = bounds.minLng; lng <= bounds.maxLng; lng += lngStep) {
      const point = { latitude: lat, longitude: lng };
      if (isPointInPolygon(point, polygon)) {
        heatmapPoints.push({ ...point, weight: 5 });
      }
    }
  }
  return heatmapPoints;
};
// Utility function to check if a point is inside the polygon
const isPointInPolygon = (point: LatLng, polygon: LatLng[]): boolean => {
  let x = point.latitude,
    y = point.longitude;
  let inside = false;
  for (let i = 0, j = polygon.length - 1; i < polygon.length; j = i++) {
    let xi = polygon[i].latitude,
      yi = polygon[i].longitude;
    let xj = polygon[j].latitude,
      yj = polygon[j].longitude;
    let intersect =
      yi > y !== yj > y && x < ((xj - xi) * (y - yi)) / (yj - yi) + xi;
    if (intersect) inside = !inside;
  }
  return inside;
};
